function hidePass() {
    var a = document.getElementById('passChanger').style.display;
    document.getElementById('passChanger').style.display = (a === 'none')?'block':'none';
}
function hideImg() {
    var a = document.getElementById('imgChanger').style.display;
    document.getElementById('imgChanger').style.display = (a === 'none')?'block':'none';
}